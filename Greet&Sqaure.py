def student_info(name, course, age=18):  # 'age' is a default argument
    print(f"Name: {name}")
    print(f"Course: {course}")
    print(f"Age: {age}")

# Positional arguments
student_info("Rahul", "B.Sc")

# Keyword arguments
student_info(course="B.Com", name="Sneha", age=21)

# Mix of positional and default
student_info("Anjali", "B.A")

print("\n------------------------------")


# b. Function returning multiple values


def calculate_marks(m1, m2, m3):
    total = m1 + m2 + m3
    average = total / 3
    grade = "A" if average >= 75 else "B" if average >= 60 else "C"
    return total, average, grade  # returning multiple values

# Unpacking multiple return values
total, average, grade = calculate_marks(80, 70, 90)
print(f"Total: {total}, Average: {average:.2f}, Grade: {grade}")

print("\n------------------------------")


# c. Lambda functions with map(), filter(), and reduce()


# Import reduce
from functools import reduce

numbers = [2, 5, 8, 10, 3, 6]

# map() â†’ apply a function to all elements
squared = list(map(lambda x: x ** 2, numbers))
print("Squared Numbers:", squared)

# filter() â†’ filter elements based on a condition
even_numbers = list(filter(lambda x: x % 2 == 0, numbers))
print("Even Numbers:", even_numbers)

# reduce() â†’ accumulate results (sum in this case)
sum_all = reduce(lambda a, b: a + b, numbers)
print("Sum of All Numbers:", sum_all)

