# a. Demonstrate local, global, and nonlocal variables


x = "Global Variable"  # Global variable

def outer_function():
    x = "Outer Variable"  # Enclosed (nonlocal) variable

    def inner_function():
        nonlocal x  # Refers to variable in outer_function
        x = "Modified by Inner Function"
        print("Inner Function:", x)

    inner_function()
    print("Outer Function:", x)

outer_function()
print("Global Scope:", x)

print("\n------------------------------")

# b. Use nested functions and variable scope

def calculator(a, b):
    def add():
        return a + b

    def multiply():
        return a * b

    def subtract():
        return a - b

    # Accessing inner functions (nested)
    print("Addition:", add())
    print("Multiplication:", multiply())
    print("Subtraction:", subtract())

calculator(10, 5)

print("\n------------------------------")


# c. Reverse string using recursion


def reverse_string(s):
    if len(s) == 0:
        return s
    else:
        return reverse_string(s[1:]) + s[0]

# Example
word = "Python"
print(f"Original String: {word}")
print(f"Reversed String: {reverse_string(word)}")

